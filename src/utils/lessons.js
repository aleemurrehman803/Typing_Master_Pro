// Track Mistakes
export const trackMistakes = (input, target) => {
  const mistakes = {};
  for (let i = 0; i < target.length; i++) {
    if (input[i] !== target[i]) {
      const key = target[i];
      mistakes[key] = (mistakes[key] || 0) + 1;
    }
  }
  return mistakes;
};

// Save Weak Keys
export const saveWeakKeys = (mistakes) => {
  let existing = JSON.parse(localStorage.getItem('weakKeys')) || {};
  for (let key in mistakes) {
    existing[key] = (existing[key] || 0) + mistakes[key];
  }
  localStorage.setItem('weakKeys', JSON.stringify(existing));
};

// Generate AI Lesson
export const generateWeakLesson = () => {
  const weakKeys = JSON.parse(localStorage.getItem('weakKeys')) || {};
  const sorted = Object.keys(weakKeys).sort((a, b) => weakKeys[b] - weakKeys[a]);
  const topKeys = sorted.slice(0, 5);

  return {
    id: 'ai1',
    title: 'AI Weak Keys Training',
    content: topKeys.join(' ') + ' ' + topKeys.join('')
  };
};

// WPM + Accuracy
export const calculateStats = (input, target, time) => {
  const words = input.length / 5;
  const wpm = Math.round((words / time) * 60);

  let correct = 0;
  for (let i = 0; i < target.length; i++) {
    if (input[i] === target[i]) correct++;
  }

  const accuracy = Math.round((correct / target.length) * 100);

  return { wpm, accuracy };
};
